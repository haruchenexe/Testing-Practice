var calculator = {
    add(value1, value2) {
        return value1 + value2
    },
    subtract(value1, value2) {
        return value1 - value2
    },
    divide(value1, value2) {
        return value1 / value2
    },
    mulltiply(value1, value2) {
        return value1 * value2
    }
}
    

module.exports = calculator